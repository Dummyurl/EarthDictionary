package com.example.android.spaceapps.SearchWord;

public class SeeAlso {

    String label, labelLink;

    SeeAlso(String label, String labelLink){
        this.label = label;
        this.labelLink = labelLink;
    }

    public String getLabel() {
        return label;
    }

    public String getLabelLink() {
        return labelLink;
    }
}
